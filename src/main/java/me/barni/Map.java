    ShaderProgram frontShader, backShader;
    VertexArrayObject vao;
    Texture[] atlas;

    public Map(int w, int h) {
        this.w = w;
        this.h = h;

        frontShader = new ShaderProgram();
        frontShader.create("mapTile");
        frontShader.link();

        backShader = new ShaderProgram();
        backShader.create("backgroundTile");
        backShader.link();
        backShader.uploadVec4("uBackColor", new Vector4f(.2f,.2f, .2f, 0));

        vao = new VertexArrayObject();
        float[] vArray = new float[8];
        vao.setVertexData(vArray);
        vao.setElementData(RenderableQuad.elementArray);
        vao.addAttributePointer(2); //Position (x,y)
        vao.addAttributePointer(2); //TX coords (u,v)

        String[] txts = {
                null,
                "dirt",
                "grass_tile",
                "grass",
                "glass",
                "planks",
                "brick",
                "water",
                "spike2",
                "cobble",
                "lava",
                "iron"};

        atlas = new Texture[txts.length];

        for (int i = 0; i < txts.length; i++) {
            if (txts[i] == null)
                continue;
            atlas[i] = new Texture();
            atlas[i].generate();
            atlas[i].loadImage(txts[i]);
            atlas[i].uploadImageToGPU(false);
            atlas[i].bind();
        }
        GL30.glEnable(GL30.GL_BLEND);
        GL30.glBlendFunc(GL30.GL_SRC_ALPHA, GL30.GL_ONE_MINUS_SRC_ALPHA);
    }

    final int TSIZE = 32;

    public void render(Camera camera) {
        vao.bind(false);


        renderTiles(false, camera);
        renderTiles(true, camera);

        vao.unBind();
    }

    private void renderTiles(boolean front, Camera camera) {
        ShaderProgram currentShader;
        if (front)
            currentShader = frontShader;
        else
            currentShader = backShader;

        currentShader.bind();

        currentShader.uploadMat4("uProjMat", camera.getProjMat());
        currentShader.uploadMat4("uViewMat", camera.getViewMat());
        currentShader.uploadFloat("uAlpha", 0);
        currentShader.uploadFloat("uTime", time);


        for (int i = 0; i < w * h; i++) {

            if (front) {
                if (tiles[i] == 0)
                    continue;
            } else if (backTiles[i] == 0)
                continue;

            float[] vArray = RenderableQuad.generateVertexArray(i % w * TSIZE,
                    i / w * TSIZE, TSIZE, TSIZE);
            vao.setVertexData(vArray);

            //frontShader.selectTextureSlot("uTexSampler", 0);

            if (front)
                atlas[tiles[i]].bind();
            else
                atlas[backTiles[i]].bind();

            GL30.glDrawElements(GL30.GL_TRIANGLES, vao.getVertexLen(), GL30.GL_UNSIGNED_INT, 0);

        }
        currentShader.unBind();
    }
